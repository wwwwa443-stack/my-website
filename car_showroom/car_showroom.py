# ---- صفحة تفاصيل السيارة ----
@app.route("/car/<int:car_id>")
def car_page(car_id):
    mode = request.args.get("mode", "light")
    car = next((c for c in cars if c["id"] == car_id), None)
    if not car:
        abort(404)
    return render_template("car.html", car=car, mode=mode)

