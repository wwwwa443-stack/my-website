@app.route("/")
def index():
    q = request.args.get("q", "").strip()
    if q:
        filtered = [c for c in cars if q.lower() in c["name"].lower()]
    else:
        filtered = cars
    return render_template("index.html", cars=filtered, q=q, mode="light")

